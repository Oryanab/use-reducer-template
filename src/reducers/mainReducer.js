// CRUD: CREATE, READ, UPDATE, DELETE
export const mainReducer = (state, action) => {
  switch (action.type) {
    case "READ_FRIEND":
      return state;
    case "CREATE_FRIEND":
      return state;
    case "UPDATE_FRIEND":
      return state;
    case "DELETE_FRIEND":
      return state;
    default:
      return state;
  }
};
